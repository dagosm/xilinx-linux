/**
 * lmx2594_update_rfreq() - Update clock multiplier
 * @data:	Driver data structure
 */
static int lmx2594_update_rfreq(struct clk_lmx2594 *data)
{
	u8 tx_array[5];
	int i=0;
	u32 val;
	int freq_index=0;

	pr_info("lmx update:  current frequency index %d max freq: %d\n",
			data->frequency, PLL_MAX);
	if (data->frequency == 0) {
		val = 0x3;
		tx_array[2] = (u8) val & (0xFF);
		tx_array[1] = (u8) val & (0xFF);
		tx_array[0] = (u8) (val >> 16) & (0xFF);
		regmap_bulk_write(data->regmap, data->chip, tx_array, 0x3);
		return 0;
	}
/*
 * 1. Apply power to device.
 * 2. Program RESET = 1 to reset registers.
 * 3. Program RESET = 0 to remove reset.
 * 4. Program registers as shown in the register map in REVERSE order from highest to lowest.
 * 5. Program register R0 one additional time with FCAL_EN = 1 to ensure that the VCO calibration runs from a
 * stable state.
 */
		val = 0x2;
		tx_array[2] = (u8) val & (0xFF);
		tx_array[1] = (u8) val & (0xFF);
		tx_array[0] = (u8) (val >> 16) & (0xFF);
		regmap_bulk_write(data->regmap, data->chip, tx_array, 0x3);
		val = 0x0;
		tx_array[2] = (u8) val & (0xFF);
		tx_array[1] = (u8) val & (0xFF);
		tx_array[0] = (u8) (val >> 16) & (0xFF);
		regmap_bulk_write(data->regmap, data->chip, tx_array, 0x3);

	for (i = 0; i < LMX2592_A_count; i++) {
		tx_array[2] = (u8) (LMX2592_A[data->frequency][i]) & (0xFF);
		tx_array[1] = (u8) (LMX2592_A[data->frequency][i] >> 8) & (0xFF);
		tx_array[0] = (u8) (LMX2592_A[data->frequency][i] >> 16) & (0xFF);
		regmap_bulk_write(data->regmap, data->chip, tx_array, 0x3);
		msleep(1);
	}

	/* FCAL_EN = 1 */
	val = 0x2;
	tx_array[2] = (u8) (LMX2592_A[data->frequency][112]) & (0xFF);
	tx_array[1] = (u8) (LMX2592_A[data->frequency][112] >> 8) & (0xFF);
	tx_array[0] = (u8) (LMX2592_A[data->frequency][112] >> 16) & (0xFF);
	regmap_bulk_write(data->regmap, data->chip, tx_array, 0x3);
	return 0;
}

static int lmk4028_update_rfreq(struct clk_lmx2594 *data)
{
	int i;
	__be32 reg;

	pr_info("lmk update:  current frequency index 0x%x\n",
			data->frequency);

	for (i = 0; i < LMK04208_count; i++) {
		reg = cpu_to_be32(LMK04208_CKin[data->frequency][i]);
		regmap_bulk_write(data->regmap, 0x2, &reg, 0x4);
		msleep(1);
	}
	return 0;
}

