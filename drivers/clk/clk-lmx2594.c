static int lmx2594_probe(struct i2c_client *client,
		const struct i2c_device_id *id)
{
	struct clk_lmx2594 *data;
	struct clk_init_data init;
	u32 initial_fout;
	int err;
	struct device *dev = &client->dev;

	data = devm_kzalloc(&client->dev, sizeof(*data), GFP_KERNEL);
	if (!data)
		return -ENOMEM;

	init.ops = &lmx2594_clk_ops;
	init.flags = 0;
	init.num_parents = 0;
	data->hw.init = &init;
	data->i2c_client = client;

	if (of_property_read_string(client->dev.of_node, "clock-output-names",
				&init.name))
		init.name = client->dev.of_node->name;

	data->regmap = devm_regmap_init_i2c(client, &lmx2594_regmap_config);
	if (IS_ERR(data->regmap)) {
		dev_err(&client->dev, "failed to allocate register map\n");
		return PTR_ERR(data->regmap);
	}

	i2c_set_clientdata(client, data);

	err = of_clk_add_hw_provider(client->dev.of_node, of_clk_hw_simple_get,
			&data->hw);
	if (err) {
		dev_err(&client->dev, "unable to add clk provider\n");
		return err;
	}

	/* Read the requested initial output frequency from device tree */
	if (!of_property_read_u32(client->dev.of_node, "clock-frequency",
				&initial_fout)) {

		err = clk_set_rate(data->hw.clk, initial_fout);
		if (err) {
			of_clk_del_provider(client->dev.of_node);
			return err;
		}
	}
	data->chip = 0xD;
	/*
	 * Create sysfs file entries for the device
	 */
	err = sysfs_create_group(&dev->kobj, &lmx_attributes);
	if (err < 0) {
		return err;
	}

	return 0;
}

static int lmx2594_remove(struct i2c_client *client)
{
	of_clk_del_provider(client->dev.of_node);
	sysfs_remove_group(&client->dev.kobj, &lmx_attributes);
	return 0;
}

