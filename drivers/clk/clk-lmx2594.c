/**
 * lmx2594_set_frequency() - Adjust output frequency
 * @data:	Driver data structure
 * @frequency:	Target frequency
 * Returns 0 on success.
 *
 * Update output frequency for big frequency changes (> 3,500 ppm).
 */
static int lmx2594_set_frequency(struct clk_lmx2594 *data, unsigned long frequency)
{
	data->frequency = frequency;
	lmk4028_update_rfreq(data);
	lmx2594_update_rfreq(data);

	/* Applying a new frequency can take up to 10ms */
	usleep_range(10000, 12000);

	return 0;
}

static int lmx2594_set_rate(struct clk_hw *hw, unsigned long rate,
		unsigned long parent_rate)
{
	struct clk_lmx2594 *data = to_clk_lmx2594(hw);
	int err;

	data->frequency = rate;
	err = lmx2594_set_frequency(data, rate);

	if (err)
		return err;


	return 0;
}

